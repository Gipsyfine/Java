package com.gipsyfine.example;

public class Example08 {
    public static void main(String[] args) {
        int num = 19;
        if (num % 2 == 0)
        {
            // 判断条件成立，num被 2 整除
            System.out.println("num是一个偶数");
        }
        else
        {
            // 判断条件不成立，num不能被 2 整除
            System.out.println("num是一个奇数");
        }
    }
}

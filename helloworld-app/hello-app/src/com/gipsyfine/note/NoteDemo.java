package com.gipsyfine.note;

/**
    目标：学习注释的使用
    这个程序就是讲解注释的
 */
public class NoteDemo {
    public static void main(String[] args) {
        // 这是单行注释
        System.out.println("现在是2023.3.9晚上8:00");
        System.out.println("在想唐晶晶");

        /*
        这是多行注释
        */
        System.out.println("唐晶晶");
        System.out.println("Tang Jingjing");
        System.out.println("T J J");
    }
}
